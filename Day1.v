Fixpoint zmax (l : list Z) (cur : Z) :=
  match l with
  | nil => cur
  | (x :: t) => zmax t (if Z.leb x cur then cur else x)
  end.

Fixpoint insert (l : list Z) (new : Z) :=
  match l with
  | nil => [new]
  | (h :: t) => if Z.geb new h then new :: h :: t else h :: insert t new
  end.

Fixpoint sort (l : list Z) :=
  match l with
  | nil => nil
  | (h :: t) => insert (sort t) h
  end.

Fixpoint zsum (l : list Z) :=
  match l with
  | nil => 0
  | (x :: t) => x + zsum t
  end.

Definition first3sum (l : list Z) :=
  match l with
  | nil => 0
  | [a] => a
  | [a;b] => a+b
  | (a :: b :: c :: _ )=> a+b+c
  end.

